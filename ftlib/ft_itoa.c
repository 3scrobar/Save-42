#include "libft.h"

char *ft_itoa(int n)
{
	char	*str;
	int		isNegative;
	int		i;

	i = 0;
	isNegative = 1;
	if (n ==-2147483648 )
		str = "-2147483648";
	if (n < 0)
	{
		isNegative = 1;
		n = n * -1;
	}
	if (isNegative)
		str[i++] = '-';
		
}
